// Helper function to get districts by province
export const getDistrictsByProvince = (province) => {
  if (!province || !nepalLocations[province]) return [];
  return Object.keys(nepalLocations[province]);
};

// Helper function to get municipalities by district
export const getMunicipalitiesByDistrict = (province, district) => {
  if (!province || !district || !nepalLocations[province] || !nepalLocations[province][district]) return [];
  return nepalLocations[province][district].municipalities;
};

// Helper function to get areas by municipality
export const getAreasByMunicipality = (province, district, municipality) => {
  if (!province || !district || !municipality) return [];
  if (!nepalLocations[province] || !nepalLocations[province][district]) return [];
  const areas = nepalLocations[province][district].areas;
  return areas[municipality] || [];
};

// Get all provinces
export const getProvinces = () => {
  return Object.keys(nepalLocations);
};

// Get statistics
export const getLocationStats = () => {
  const provinces = Object.keys(nepalLocations);
  let totalDistricts = 0;
  let totalMunicipalities = 0;
  let totalAreas = 0;

  provinces.forEach(province => {
    const districts = Object.keys(nepalLocations[province]);
    totalDistricts += districts.length;
    
    districts.forEach(district => {
      const municipalities = nepalLocations[province][district].municipalities;
      totalMunicipalities += municipalities.length;
      
      const areas = nepalLocations[province][district].areas;
      Object.keys(areas).forEach(municipality => {
        totalAreas += areas[municipality].length;
      });
    });
  });

  return {
    provinces: provinces.length,
    districts: totalDistricts,
    municipalities: totalMunicipalities,
    areas: totalAreas
  };
};
